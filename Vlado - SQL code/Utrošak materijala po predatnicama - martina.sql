USE FeroApp

SELECT ID_Skl, (SELECT ID_Mat FROM IzlazRobe WHERE ID_IZR = UlazProizvoda.ID_IZR) AS ID_Mat, ID_Pro, BrojRN, KolicinaMat, KolicinaPro, OtpadMat, OtpadObrada1, OtpadObrada2, OtpadObrada3, OtpadObrada4, OtpadObrada5 
	INTO TempMartina2014
	FROM UlazProizvoda 
	WHERE ID_PRE IN(SELECT ID_PRE FROM Predatnice WHERE DatumDokumenta BETWEEN '2014-04-01' AND '2014-06-30')
	AND ID_IZR IN(SELECT ID_IZR FROM IzlazRobeDetaljnoView WHERE ID_Mat IN(920007,920017,920018,920022,920024,920048,920068,920076,920077,920079,920081,920087,920088,920095,920096,920096,920108,
	920120,920121,920122,920129,920132,920133,920136,920144,920148,920151,920161,920168,920184,920185,920189,920194,920195,
	920196,920201,920203,920207,920215,920265,920267,920281,920282,920302,920304) AND VrstaDokumenta = 'Izdatnica')

SELECT ID_Skl, ID_Mat, ID_Pro, BrojRN, SUM(KolicinaMat) AS KolMat, SUM(KolicinaPro) AS KolPro, 
	SUM(OtpadMat) AS Otpadmat, SUM(OtpadObrada1) AS OtpadTok, SUM(OtpadObrada2) AS OtpadKalj, SUM(OtpadObrada3) AS OtpadTT, SUM(OtpadObrada4) AS Otpad4, SUM(OtpadObrada5) AS Otpad5
	FROM TempMartina2014
	GROUP BY ID_Skl, ID_Mat, ID_Pro, BrojRN
	ORDER BY ID_Skl, ID_Mat, ID_Pro, BrojRN